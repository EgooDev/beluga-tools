def check_subdomain(domain_name, subdomain):
    url = f"https://{subdomain}.{domain_name}"
    try:
        requests.get(url, timeout=TIMEOUT)
        return f"[+] {url}"
    except requests.RequestException:
        return None

def domain_scanner(domain_name, subdomains, max_threads=100):
    print("Scanning, please wait...\n")
    with ThreadPoolExecutor(max_workers=max_threads) as executor:
        futures = [executor.submit(check_subdomain, domain_name, sub) for sub in subdomains]
        for future in futures:
            result = future.result()
            if result:
                print(result)

